public class Associado {

    private String nome;
	private int id;
	
	
	
	public Associado(String nome, int id) {
		this.nome = nome;
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setTitulo(String nome) {
		this.nome = nome;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}